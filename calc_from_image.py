def pmt(P, apr, n=12):
    mr = apr / 12
    return P * mr * (1 + mr) ** n / ((1 + mr) ** n - 1)


# 全部从截图读取，13笔（加总=310943.38 已验证）
loans = [
    # (date, principal, period k/12, rate, rem_from_image)
    ("2025-08-26", 129000, 10, 0.09, 33626.91),
    ("2025-08-28", 10000, 10, 0.09, 2605.66),
    ("2025-09-01", 59900, 10, 0.09, 15539.44),
    ("2026-02-05", 33000, 5, 0.135, 2445.97),
    ("2026-02-10", 92100, 5, 0.135, 62787.14),
    ("2026-03-02", 30000, 4, 0.135, 22970.74),
    ("2026-03-04", 25000, 4, 0.135, 19128.29),
    ("2026-03-05", 50000, 4, 0.135, 38231.99),
    ("2026-03-06", 174900, 4, 0.135, 28451.98),
    ("2026-03-30", 10000, 3, 0.135, 8465.64),
    ("2026-03-31", 30000, 3, 0.135, 25387.61),
    ("2026-04-03", 50700, 3, 0.135, 42858.02),
    ("2026-04-06", 10000, 3, 0.135, 8443.99),
]

print("从截图直接算：总利息 = 已还 + 图里待还 - 本金")
print("=" * 90)
print(f"{'#':>2} {'日期':<12} {'本金':>8} {'期':>5} {'利率':>5} {'已还(估)':>10} {'图待还':>10} {'总还款':>10} {'总利息':>10}")
print("-" * 90)

total_p = 0
total_int = 0
total_paid = 0
total_rem = 0

for i, (d, P, k, apr, rem) in enumerate(loans, 1):
    m = pmt(P, apr)
    paid = m * (k - 1)  # 已完成 k-1 期
    total_pay = paid + rem  # 用图里待还，不用公式
    interest = total_pay - P
    total_p += P
    total_int += interest
    total_paid += paid
    total_rem += rem
    print(
        f"{i:>2} {d} {P:>8} {k:>2}/12 {apr*100:>4.1f}% "
        f"{paid:>10.2f} {rem:>10.2f} {total_pay:>10.2f} {interest:>10.2f}"
    )

print("-" * 90)
print(f"合计  本金 {total_p:,}  图待还 {total_rem:,.2f}  已还 {total_paid:,.2f}")
print(f"京东总利息(从图反推) = {total_int:,.2f} 元")
print(f"利息/本金 = {total_int/total_p*100:.2f}%")
print()
print("对比：若用公式'还满12期'全期利息 = 47,566.71")
print(f"从图反推实际总利息   = {total_int:,.2f}  (差额 {47566.71-total_int:,.2f})")

